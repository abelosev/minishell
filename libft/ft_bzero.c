#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*s1;

	i = 0;
	if (n == 0)
		return ;
	s1 = (unsigned char*)s;
	while (i < n)
	{
		s1[i] = '\0';
		i++;
	}
}
